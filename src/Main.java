public class Main {
    public static void main(String[] args) {
        System.out.println("Task1");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println("Task2");
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }

        System.out.println("Task3");
        for (int a = 0; a <= 17; a = a + 2) {
            System.out.println(a);
        }
    }
}